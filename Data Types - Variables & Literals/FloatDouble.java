//Float and Java Literal

public class FloatDouble {
    public static void main(String arg[]) {
        // float f=12.56; Error. By default, floating-point numbers are treated as double in Java.
        // We write f or F at the end of the number to treat it as float.
        float f=12.56f;
        double d=12.56;
        System.out.println(f);
        System.out.println(d);
    }
    
}
